c###setgph.for
      SUBROUTINE SETGPH
C--------------------------------
C
C     THIS ROUTINE SETS THE VARIABLES TO BE PLOTTED DEPENDENT ON METHOD
C     SUBROUTINE GPHBOD CREATES LINE PRINTER GRAPHS OF THE VARIABLES SET
C
      COMMON / ION / IHRE, IHRO, IHRS, LUFP, METHOD, NOISE, NPSL
      COMMON/MUFS/EMUF(24),F1MUF(24),F2MUF(24),ESMUF(24),ALLMUF(24)
     1,FOT(24),XLUF(24),HPF(24),ANGMUF(24),MODMUF,SIGL(4),SIGU(4),DELMU
     2F(4),HPMUF(4),HTMUF(4),FVMUF(4),AFMUF(4),NHOPMF(4),YFOT(4),YHPF(4)
     3 ,YMUF(4)
      COMMON / OUTGH / PLOTI(24), PLOTJ(24), PLOTK(24), XOUT(13), NPLOTS
      COMMON / OUTLAB / LABEL(11), LAYTYP(5), IEAST, INORTH, ISOUTH,
     A IWEST, LABLI, LABLJ, LABLK
      CHARACTER IEAST*1, ISOUTH*1, INORTH*1, IWEST*1, LABEL*5, LAYTYP*2,
     A LABLI*5, LABLJ*5, LABLK*5
      DATA NHR/24/
C.....SET FOR MUF-FOT GRAPH
C.....CHANGE VARIABLES AND/OR ADD THIRD VARIABLE DEPENDENT OF METHOD.
      NPLOTS = 2
      LABLI = LABEL(10)
      LABLJ = LABEL(4)
      LABLK = LABEL(11)
      DO 80 ICR = 1,NHR
      PLOTI(ICR) = ALLMUF(ICR)
   80 PLOTJ(ICR) = FOT(ICR)
      IF(METHOD - 4) 90, 400, 90
   90 IF(METHOD - 8) 100, 400, 100
C.....SET FOR MUF-FOT-HPF GRAPH
  100 IF(METHOD - 5) 110, 120, 110
  110 IF(METHOD - 9) 140, 120, 140
  120 NPLOTS = 3
      LABLK = LABEL(7)
      DO 130 ICR = 1,NHR
  130 PLOTK(ICR) = HPF(ICR)
      GO TO 400
C.....SET FOR MUF-FOT-ES GRAPH
  140 IF(METHOD - 6) 150, 160, 150
  150 IF(METHOD - 11) 180, 160, 180
  160 NPLOTS = 3
      LABLK = LABEL(3)
      DO 170 ICR = 1,NHR
  170 PLOTK(ICR) = ESMUF(ICR)
      GO TO 400
C.....SET FOR MUF-FOT-ANG GRAPH
  180 IF(METHOD - 10) 210, 190, 210
  190 NPLOTS = 3
      LABLK = LABEL(1)
      DO 200 ICR = 1,NHR
  200 PLOTK(ICR) = ANGMUF(ICR)
      GO TO 400
C.....SET FOR LUF-FOT GRAPH
  210 IF(METHOD - 27) 240, 220, 240
  220 LABLI = LABEL(8)
      DO 230 ICR = 1,NHR
  230 PLOTI(ICR) = XLUF(ICR)
      GO TO 400
C.....SET FOR MUF-FOT-LUF GRAPH
  240 IF(METHOD - 28) 270, 250, 270
  250 NPLOTS = 3
      LABLK = LABEL(8)
      DO 260 ICR = 1,NHR
  260 PLOTK(ICR) = XLUF(ICR)
      GO TO 400
C.....SET FOR MUF-LUF GRAPH
  270 IF(METHOD - 29) 300, 280, 300
  280 LABLJ = LABEL(8)
      DO 290 ICR = 1,NHR
  290 PLOTJ(ICR) = XLUF(ICR)
      GO TO 400
C.....THE METHOD SPECIFIED DOESN"T PRODUCE GRAPHS FROM THIS ROUTINE
  300 NPLOTS = 0
  400 CONTINUE
      RETURN
      END
C--------------------------------
